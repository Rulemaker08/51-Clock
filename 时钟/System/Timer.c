#include <REGX52.H>
#include "Timer.h"

/**
   * @brief  定时器初始化
   * @param	 无
   * @retval 无
**/
void Timer_Init()		
{
	TMOD |= 0x11;				//设置定时器模式
	
	TL0 = (65536-100)%256;		//设置定时0初值
	TH0 = (65536-100)/256;		//设置定时0初值
	TF0 = 0;					//清除定时器溢出标志	
	TR0=1;						//定时器0开始计时
	ET0=1;						//定时器0中断使能
	
	TL1 = (65536-11)%256;		//设置定时1初值
	TH1 = (65536-11)/256;		//设置定时1初值
	TF1 = 0;					//清除定时器1标志	
	TR1=1;						//定时器1开始计时
	ET1=1;						//定时器1中断使能
	
	EA=1;						//打开总中断
}


